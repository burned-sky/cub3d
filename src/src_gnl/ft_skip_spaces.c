#include "read_cub.h"

void	ft_skip_spaces(char *line, int *i)
{
	while (line[*i] == ' ' || line[*i] == '\n' || line[*i] == '\f' ||
	line[*i] == '\r' || line[*i] == '\t' || line[*i] == '\v')
		*i += 1;
}
